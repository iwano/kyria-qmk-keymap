bool process_record_user(uint16_t keycode, keyrecord_t *record) {
    // pick random image on first key press
    if (img_idx == -1) {
        img_idx = timer_read32() % 2;
    }

    switch (keycode) {
    case SELECT_WORD:
        if (record->event.pressed) {
            // when keycode SELECT_WORD is pressed
            register_code(KC_LEFT_ALT);
            SEND_STRING(SS_TAP(X_LEFT));
            unregister_code(KC_LEFT_ALT);
            register_code(KC_LEFT_ALT);
            register_code(KC_LSFT);
            SEND_STRING(SS_TAP(X_RIGHT));
            unregister_code(KC_LEFT_ALT);
            unregister_code(KC_LSFT);
        } else {
            // when keycode SELECT_WORD is released
        }
        break;
     case SELECT_LINE:
        if (record->event.pressed) {
            // when keycode SELECT_LINE is pressed
            register_code(KC_LGUI);
            SEND_STRING(SS_TAP(X_LEFT));
            unregister_code(KC_LGUI);
            register_code(KC_LGUI);
            register_code(KC_LSFT);
            SEND_STRING(SS_TAP(X_RIGHT));
            unregister_code(KC_LGUI);
            unregister_code(KC_LSFT);
        } else {
            // when keycode SELECT_LINE is released
        }
        break;
    }
    return true;
};

// Data sync
typedef struct _master_to_slave_t {
    int m2s_data;
} master_to_slave_t;

void user_sync_a_slave_handler(uint8_t in_buflen, const void* in_data, uint8_t out_buflen, void* out_data) {
    const master_to_slave_t *m2s = (const master_to_slave_t*)in_data;
    img_idx = m2s->m2s_data;
}

void keyboard_post_init_user(void) {
    transaction_register_rpc(USER_SYNC_A, user_sync_a_slave_handler);
}

void housekeeping_task_user(void) {
    if (is_keyboard_master()) {
        // Interact with slave every 500ms,
        static uint32_t last_sync = 0;
        if (img_idx == -1) return;

        if (timer_elapsed32(last_sync) > 500) {
            master_to_slave_t m2s = {img_idx};
            if(transaction_rpc_send(USER_SYNC_A, sizeof(m2s), &m2s)) {
                last_sync = timer_read32();
            }
        }
    }
}